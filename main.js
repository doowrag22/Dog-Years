var myAge = 40;
//creating variable myAge and marking as my actual age 40

var earlyYears = 2;
//creating earlyYears variable

earlyYears *= 10.5;
//multiplying earlyYears by 10.5 and reassigning that value to earlyYears

laterYears = myAge - 2;
//taking earlyYears out of my age

laterYears *= 4;

myAgeInDogYears = earlyYears + laterYears;
//calculating my age in dogyears

myName = 'Bryan'.toLowerCase();
//creating myName variable and moving to all lowercase

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
